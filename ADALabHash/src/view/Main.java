    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {
      String username = jTextFieldAddUser.getText();
      if (username.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Debes rellenar el campo de nombre", "Error", JOptionPane.ERROR_MESSAGE);
      }

      this.hashTableAB.addAB(new NodoAB(username));
      this.hashTableRL.addRL(username);

      JOptionPane.showMessageDialog(null, "Usuario agregado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);

      jTextFieldAddUser.setText("");
    }

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {
      String username = jTextFieldSearchUser.getText();

      if (username.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Debes rellenar el campo de nombre", "Error", JOptionPane.ERROR_MESSAGE);
      }

      String nameFound = this.hashTableRL.buscarRL(username);
      NodoAB response = this.hashTableAB.buscarAB(username);
      if (response == null) {
        JOptionPane.showMessageDialog(null, "No encontrado", "Busqueda", JOptionPane.INFORMATION_MESSAGE);
      } else {
        String toShow = "Encontrado: \nHash Table con Arboles Binarios: \n" + response.getName() + "\nHash Table con Resoluci√≥n Lineal: \n" + nameFound;
        JOptionPane.showMessageDialog(null, toShow, "Busqueda", JOptionPane.INFORMATION_MESSAGE);
      }

      jTextFieldSearchUser.setText("");
    }

